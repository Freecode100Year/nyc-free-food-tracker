#!/bin/bash

echo "🔧 配置 Cloudflare Wrangler"
echo "============================"

# 检查是否已安装 wrangler
if ! command -v wrangler &> /dev/null; then
    echo "安装 Wrangler..."
    npm install -g wrangler
fi

echo ""
echo "📋 配置步骤："
echo ""
echo "1. 创建 Cloudflare API 令牌（如果你还没有）："
echo "   访问: https://dash.cloudflare.com/profile/api-tokens"
echo "   点击 'Create Token'"
echo "   选择 'Edit Cloudflare Workers' 模板"
echo "   权限设置："
echo "     - Account → Workers Scripts → Edit"
echo "     - Account → Workers Routes → Edit"
echo "     - Zone → Workers Routes → Edit"
echo "   资源：Include → All accounts"
echo "   创建并复制令牌"
echo ""
echo "2. 运行 Wrangler 配置命令："
echo "   wrangler config"
echo ""
echo "3. 输入以下信息："
echo "   Email: sj9292008133@gmail.com"
echo "   API Token: [粘贴你的 API 令牌]"
echo ""
echo "4. 验证配置："
echo "   wrangler whoami"
echo ""
echo "5. 部署到 Cloudflare Pages："
echo "   wrangler pages deploy cloudflare-pages --project-name=nyc-free-food-tracker"
echo ""
echo "📝 或者使用环境变量配置："
echo ""
echo "创建 .env 文件："
echo "CLOUDFLARE_EMAIL=sj9292008133@gmail.com"
echo "CLOUDFLARE_API_KEY=你的API密钥"
echo "CLOUDFLARE_ACCOUNT_ID=你的账户ID"
echo ""
echo "然后运行："
echo "export CLOUDFLARE_ACCOUNT_ID=\$(wrangler whoami | grep 'Account ID' | awk '{print \$3}')"
echo "wrangler pages deploy cloudflare-pages --project-name=nyc-free-food-tracker"
echo ""
echo "⚠️ 重要安全提示："
echo "- 不要将 .env 文件提交到 Git"
echo "- 定期轮换 API 密钥"
echo "- 使用最小权限原则"
echo ""
echo "🚀 开始配置..."